#define LCDWrite SPI_HF_Put
    
void LCDWriteCMD(uint8 data){
    PIN_DC = LCD_COMMAND;
    PIN_SCE = 0;
    SPI_HF_Put(data);
    PIN_SCE = 1;
    PIN_DC = LCD_DATA;
}

void LCD_XY(uint8 x, uint8 y) {
    xp = x;
    yp = y;
    LCDWriteCMD((uint8) (0x80 | x)); // Column 0
    LCDWriteCMD((uint8) (0x40 | y)); // Row y
}

void LCD_ClearLine(void) {
    uint8 i;
    PIN_SCE = 0;
    for (i = 0; i < 84; i++)
    {
        LCDWrite(0);
    }
    PIN_SCE = 1;
}

void LCD_ClearLCD(void) {
    sint16 index = 504;
    PIN_SCE = 0;
    while(index--)
    {
       LCDWrite(0) ;
    }
    PIN_SCE = 1;
}

void LCD_LargeDigit(uint8 dig) {
    uint8 i;
    uint16 offs = dig;
    offs *=36;
    PIN_SCE = 0;
    i = 12;
    while(i--)
    {
        LCDWrite(LARGE[offs++]);
    }
    PIN_SCE = 1;
    LCD_XY(xp,yp + 1);
    PIN_SCE = 0;
    i = 12;
    while (i--){
        LCDWrite(LARGE[offs++]);
    }
    PIN_SCE = 1;
    LCD_XY(xp, yp + 1);
    PIN_SCE = 0;
    i = 12;
    while (i--){
        LCDWrite(LARGE[offs++]);
    }
    PIN_SCE = 1;
}

