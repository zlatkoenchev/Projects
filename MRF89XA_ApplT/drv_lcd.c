void LCD_BCDNumber(uint8 digit, uint8 bLzero) {
    u8temp = digit >> 4;
    if ((u8temp) || (bLzero)) {
        LCD_Char(u8temp | 0x30);
    } else {
        LCD_Char(' '); //space 
    }

    u8temp = digit & 15;
    LCD_Char(u8temp | 0x30);
}

void LCD_NumberXX(uint8 digit, uint8 bLzero) {
    u8temp = digit / 10;
    if ((u8temp) || (bLzero)) {
        LCD_Char(u8temp + 0x30);
    } else {
        LCD_Char(' '); //space 
    }

    u8temp = digit % 10;
    LCD_Char(u8temp + 0x30);
}

void LCD_BitmapV(uint8 bmp){
    PIN_SCE = 0;
    LCDWrite(bmp);
    PIN_SCE = 1;
}

void LCD_Char(uint8 character) {
    uint16 index= character - 0x20;
    index *= 5;
    PIN_SCE = 0;
    for (uint8 j = 5; j != 0; j--)
    {
        LCDWrite(ASCII[index++]);
    }
    LCDWrite( 0x00);
    PIN_SCE = 1;
}

void LCD_String(const char *characters) {
    while (*characters)
        LCD_Char(*characters++);
}

void LCD_Init(void) {
    
    PIN_SCE = 1;
    
    //Reset the LCD to a known state
    PIN_RESET =0;
    PIN_RESET =1;

    LCDWriteCMD( 0x21); //Tell LCD that extended commands follow
    LCDWriteCMD( 0xa5); //Set LCD Vop (Contrast): Try 0xB1(good @ 3.3V) or 0xBF if your display is too dark
    LCDWriteCMD( 0x04); //Set Temp coefficent
    LCDWriteCMD( 0x13); //LCD bias mode 1:48: Try 0x13 or 0x14
    LCDWriteCMD( 0x20); //We must send 0x20 before modifying the display control mode
    LCDWriteCMD( 0x0c); //Set display control, normal mode. 0x0D for inverse
    
}
