void ProcessIO(void)
{   
    uint8 i,m,j ;
    //Blink the LEDs according to the USB device status
    //BlinkUSBStatus();
    // User Application USB tasks
#ifndef RX_TEST    
   if((USBDeviceState < CONFIGURED_STATE)||(USBSuspendControl==1)) return;

		HF_TxLen = getsUSBUSART(HF_Out_Data,100); //until the buffer is free.
		i=0;
        
        while (i < HF_TxLen)
		{
            if (HF_Out_Data[i] & 0x80) {
                rxpos = 0;//start from beg
                if (HF_Out_Data[i] & 0x10) {
                    cmdrcv = 1;//command
                }
                else {cmdrcv = 0;}
            }
            if (HF_Out_Data[i] & 0x20)//lsb
            {
                TxPacket[rxpos++] |= (HF_Out_Data[i] & 0x0F);
            } else {
                TxPacket[rxpos] = ((HF_Out_Data[i] << 4) & 0xF0);
            }
            if (HF_Out_Data[i] & 0x40) {
                if (1 == cmdrcv)  {
                    //command
                    cmdrcv = 0;
                    if (TxPacket[1] == 1) {
                        MRF89XASetRFMode(RF_STANDBY);
                        MRF89XARegisterSet(REG_SYNCBYTE0, TxPacket[2]);
                        MRF89XARegisterSet(REG_SYNCBYTE1, TxPacket[3]);
                        MRF89XARegisterSet(REG_SYNCBYTE2, TxPacket[4]);
                        MRF89XARegisterSet(REG_SYNCBYTE3, TxPacket[5]);
                        MRF89XARegisterSet(REG_NODEADDRESS, TxPacket[6]);
                        MRF89XARegisterSet(REG_PKTPARAM2,(PKT_FORMAT_VARIABLE | PREAMBLE_SIZE_4 | WHITENING_ON | CRC_ON | ADRSFILT_NONE ));
                        MRF89XAStartReception();
                    }
                    if (TxPacket[1] == 2) {
                        MRF89XASetRFMode(RF_STANDBY);
                        MRF89XARegisterSet(TxPacket[2], TxPacket[3]);
                        MRF89XAStartReception();
                    }
                }
                else   {
                    mLED_1 = 0;
                    cc = TxPacket[3];
                    MRF89XASendFrame();
                    while (MRF89XAIsBusy()) {};
                    MRF89XAStartReception();
                    mLED_1 = 1;
                }
                rxpos = 0; //start from beg of buffer;
            }
            i++;
       	}	
#endif    

    MRF89XATask();
    if ((RxDataLen) && (USBUSARTIsTxTrfReady())) {
        mLED_2 = 0;
        m = 0x80;
        txpos = 0;
        j = RxDataLen+1;
        RxPacket[j] = MRF89XA_RSSI;
        RxPacket[3] = cc;
        for (i = 0; i <= j; i++) {
            USB_Out_Buffer[txpos++] = (RxPacket[i]>>4)|m;
            m = 0x20;
            USB_Out_Buffer[txpos++] = (RxPacket[i] & 0x0F) | m;
            m = 0;
        }
        USB_Out_Buffer[--txpos] |= 0x40;
        NextUSBOut = txpos+1;
        Txc++;
        MRF89XAStartReception();
    }
#ifndef  RX_TEST
	if((USBUSARTIsTxTrfReady()) && (NextUSBOut > 0))
	{
		putUSBUSART(&USB_Out_Buffer[0], NextUSBOut);
		NextUSBOut = 0;
        mLED_2 = 1;
	}

    CDCTxService();
#endif
}		//end ProcessIO

